package Utility;

public class ColorRGB {
    
    private int r;
    private int g;
    private int b;

    public ColorRGB(int red, int green, int blue) {
        this.r = red;
        this.g = green;
        this.b = blue;
    }

    public int getR() {
        return r;
    }

    public int getG() {
        return g;
    }

    public int getB() {
        return b;
    }
}
